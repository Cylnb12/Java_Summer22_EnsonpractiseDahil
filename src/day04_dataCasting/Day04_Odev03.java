package day04_dataCasting;

public class Day04_Odev03 {
    public static void main(String[] args) {
        double sayi=255.36;
        System.out.println("sayi = " + sayi);
        int sayi1= (int) sayi;
        System.out.println("sayi1 = " + sayi1);
        byte sayi2= (byte) sayi1;
        System.out.println("sayi2 = " + sayi2);


    }
}
