package day04_practice;



public class Q05_ForLoop {
    public static void main(String[] args) {

        // 0-255 e kadar olan harflerin ,
        // sayı ve harf değerini ekrana yazdırınız.





        for (int i = 0; i <= 255; i++) {
            System.out.println(i+": "+(char)i);
        }


    }
}
